# 例子 1: 基本輸出
print("Hello, World!")

# 例子 2: 輸出變數
name = "Ella"
age = 20
print(name)
print(age)

# 例子 3: 輸出多個值（用逗號分隔）
print("My name is", name, "and I am", age, "years old")

# 例子 4: 使用字串格式化（f-string）
print(f"My name is {name} and I am {age} years old")

# 例子 5: 輸出列表或多行文字
hobbies = ["reading", "coding", "gaming"]
print("My hobbies are:")
for hobby in hobbies:
    print(f"  - {hobby}")